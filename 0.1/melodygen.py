# Define a function to generate a random pitch from the trichords and tetrachords
def random_pitch():
    if random.random() < 0.5:
        return random.choice(trichords)
    else:
        return random.choice(tetrachords)

# Define the melody

    
def make_melody():
    
    melody = []

    num_notes = random.randint(7, 30)

    while len(melody) < num_notes:
        # Choose a random pitch
        pitch = random_pitch()
        # Choose a random duration (can be a whole, half, quarter, or eighth note)
        duration = random.choice([0.125, 0.25, 0.375, 0.5, 0.625, 0.75, 0.875, 1, 1.125, 1.25, 1.375, 1.5, 1.625, 1.75, 1.875, 2])
        # Append the pitch and duration to the melody
        melody.append((pitch[random.randint(0, len(pitch)-1)], duration))
        # Randomly add a rest with a probability of 0.3
        if random.random() < 0.3:
            melody.append((None, duration))
    return melody

